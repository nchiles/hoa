// Regenerates the Summer Meadows parcel seed migration from the committed
// GeoJSON (scripts/data/summer_meadows_parcels.geojson, pulled once from
// Kent County's free ParcelsWithCondos FeatureServer). Run:
//   node scripts/gen-parcel-migration.mjs
// Deterministic — safe to re-run if the source GeoJSON is refreshed.

import { readFileSync, writeFileSync } from "node:fs";

const SRC = "scripts/data/summer_meadows_parcels.geojson";
const OUT = "supabase/migrations/20260517000000_summer_meadows_parcels.sql";

const fc = JSON.parse(readFileSync(SRC, "utf8"));

const title = (s) =>
  s
    .toLowerCase()
    .replace(/\b\w/g, (c) => c.toUpperCase())
    .trim()
    // Keep street directionals uppercase: "Ave Ne" -> "Ave NE".
    .replace(/\b(N|S|E|W|NE|NW|SE|SW)\b/gi, (m) => m.toUpperCase());

const rows = [];
for (const f of fc.features) {
  if (!f.geometry) continue;
  const addrRaw = String(f.properties.PROPERTYADDRESS ?? "").trim();
  const sp = addrRaw.indexOf(" ");
  const streetNumber = sp === -1 ? addrRaw : addrRaw.slice(0, sp);
  const streetName = sp === -1 ? "" : title(addrRaw.slice(sp + 1));
  // PPN comes through as a number like 411035127009 — stable parcel id.
  const pin = String(f.properties.PPN).replace(/\.0+$/, "");
  rows.push({
    pin,
    lot_number: pin, // board can rename in the UI; PPN guarantees uniqueness
    street_number: streetNumber,
    street_name: streetName,
    geom: JSON.stringify(f.geometry),
  });
}

const sql = `-- 20260517000000_summer_meadows_parcels.sql
-- GENERATED by scripts/gen-parcel-migration.mjs from a one-time pull of the
-- ${rows.length} Summer Meadows parcels (Summerwind/Summerlane Ave NE, Grand
-- Rapids, Kent County MI) off Kent County's free ParcelsWithCondos
-- FeatureServer. Lot polygons live in our DB; the map renders from here —
-- no external GIS dependency at runtime.

alter table public.lots add column if not exists geometry_geojson jsonb;
alter table public.lots add column if not exists parcel_pin text;

-- Plain (non-partial) unique index so ON CONFLICT (parcel_pin) can infer
-- it. Postgres treats NULLs as distinct, so other lots with no parcel_pin
-- are unaffected.
create unique index if not exists lots_parcel_pin_key
  on public.lots (parcel_pin);

${rows
  .map(
    (r) => `insert into public.lots
  (lot_number, street_number, street_name, parcel_pin, geometry_geojson)
values
  ('${r.lot_number}', '${r.street_number}', ${
    r.street_name ? `'${r.street_name.replace(/'/g, "''")}'` : "''"
  }, '${r.pin}', $geo$${r.geom}$geo$::jsonb)
on conflict (parcel_pin) do update set
  street_number   = excluded.street_number,
  street_name     = excluded.street_name,
  geometry_geojson = excluded.geometry_geojson;`,
  )
  .join("\n\n")}
`;

writeFileSync(OUT, sql);
console.log(`Wrote ${OUT} with ${rows.length} parcels.`);
